# –ü—Ä–æ–≥—Ä–∞–º–∞ –ø—Ä–∏–π–º–∞—î —Ä–µ—á–µ–Ω–Ω—è, —Ä–∞—Ö—É—î —á–∞—Å—Ç–æ—Ç–∏ —É–Ω—ñ–∫–∞–ª—å–Ω–∏—Ö —Å–ª—ñ–≤,
# –∞ —Ç–∞–∫–æ–∂ –æ–±—á–∏—Å–ª—é—î –∑–∞–≥–∞–ª—å–Ω—É –≤–∞—Ä—Ç—ñ—Å—Ç—å –∑–∞–ø–∞—Å—ñ–≤ —Ç–æ–≤–∞—Ä—ñ–≤ –∑—ñ —Å–∫–ª–∞–¥—É.

# –í–≤—ñ–¥ —Ä–µ—á–µ–Ω–Ω—è –∑ –∫–ª–∞–≤—ñ–∞—Ç—É—Ä–∏
sentence = input("–í–≤–µ–¥—ñ—Ç—å —Ä–µ—á–µ–Ω–Ω—è: ")

# –ü–µ—Ä–µ—Ç–≤–æ—Ä–µ–Ω–Ω—è —Ä–µ—á–µ–Ω–Ω—è —É —Å–ø–∏—Å–æ–∫ —Å–ª—ñ–≤ —É –Ω–∏–∂–Ω—å–æ–º—É —Ä–µ–≥—ñ—Å—Ç—Ä—ñ
words = sentence.lower().split()

# –°—Ç–≤–æ—Ä—é—î–º–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ª–æ–≤–Ω–∏–∫ –¥–ª—è –ø—ñ–¥—Ä–∞—Ö—É–Ω–∫—É —É–Ω—ñ–∫–∞–ª—å–Ω–∏—Ö —Å–ª—ñ–≤
word_count = {}

# –ü—Ä–æ—Ö–æ–¥–∏–º–æ –ø–æ –∫–æ–∂–Ω–æ–º—É —Å–ª–æ–≤—É –∑—ñ —Å–ø–∏—Å–∫—É
for word in words:
    # –û—á–∏—â—É—î–º–æ —Å–ª–æ–≤–æ –≤—ñ–¥ —Ä–æ–∑–¥—ñ–ª–æ–≤–∏—Ö –∑–Ω–∞–∫—ñ–≤ (.,!? —Ç–∞ —ñ–Ω.)
    word = word.strip(".,!?\"'")

    # –Ø–∫—â–æ —Å–ª–æ–≤–æ –Ω–µ –ø–æ—Ä–æ–∂–Ω—î (—Ç–æ–±—Ç–æ –Ω–µ –±—É–ª–æ –ª–∏—à–µ —Ä–æ–∑–¥—ñ–ª–æ–≤–∏–º –∑–Ω–∞–∫–æ–º)
    if word:
        # –Ø–∫—â–æ —Å–ª–æ–≤–æ –≤–∂–µ —î –≤ —Å–ª–æ–≤–Ω–∏–∫—É ‚Äî –¥–æ–¥–∞—î–º–æ 1 –¥–æ –ª—ñ—á–∏–ª—å–Ω–∏–∫–∞
        # –Ø–∫—â–æ –Ω–µ–º–∞ ‚Äî –¥–æ–¥–∞—î–º–æ –π–æ–≥–æ –∑—ñ –∑–Ω–∞—á–µ–Ω–Ω—è–º 1
        word_count[word] = word_count.get(word, 0) + 1

# –í–∏–≤–æ–¥–∏–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø—ñ–¥—Ä–∞—Ö—É–Ω–∫—É —É–Ω—ñ–∫–∞–ª—å–Ω–∏—Ö —Å–ª—ñ–≤
print("\nüìö –£–Ω—ñ–∫–∞–ª—å–Ω—ñ —Å–ª–æ–≤–∞ —Ç–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å —ó—Ö –≤–∂–∏–≤–∞–Ω—å:")
print(word_count)

# –°–ª–æ–≤–Ω–∏–∫ —ñ–∑ –∫—ñ–ª—å–∫—ñ—Å—Ç—é —Ç–æ–≤–∞—Ä—ñ–≤ –Ω–∞ —Å–∫–ª–∞–¥—ñ
stock = {
    "banana": 6,
    "apple": 0,
    "orange": 32,
    "pear": 15
}

# –°–ª–æ–≤–Ω–∏–∫ —ñ–∑ —Ü—ñ–Ω–∞–º–∏ –∑–∞ –æ–¥–∏–Ω–∏—Ü—é –∫–æ–∂–Ω–æ–≥–æ —Ç–æ–≤–∞—Ä—É
prices = {
    "banana": 4,
    "apple": 2,
    "orange": 1.5,
    "pear": 3
}

# –°—Ç–≤–æ—Ä—é—î–º–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ª–æ–≤–Ω–∏–∫ –¥–ª—è –∑–±–µ—Ä—ñ–≥–∞–Ω–Ω—è —Å—É–º–∞—Ä–Ω–æ—ó –≤–∞—Ä—Ç–æ—Å—Ç—ñ –∫–æ–∂–Ω–æ–≥–æ —Ç–æ–≤–∞—Ä—É
inventory_value = {}

# –û–±—á–∏—Å–ª—é—î–º–æ —Å—É–º–∞—Ä–Ω—É –≤–∞—Ä—Ç—ñ—Å—Ç—å –∫–æ–∂–Ω–æ–≥–æ —Ç–æ–≤–∞—Ä—É —è–∫: –∫—ñ–ª—å–∫—ñ—Å—Ç—å * —Ü—ñ–Ω–∞
for item in stock:
    # –ë–µ—Ä–µ–º–æ –∫—ñ–ª—å–∫—ñ—Å—Ç—å —ñ–∑ stock —Ç–∞ —Ü—ñ–Ω—É –∑ prices
    # –Ø–∫—â–æ —Ü—ñ–Ω–∞ –Ω–µ –∑–Ω–∞–π–¥–µ—Ç—å—Å—è –≤ —Å–ª–æ–≤–Ω–∏–∫—É ‚Äî –±–µ—Ä–µ–º–æ 0 (—â–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ –ø–æ–º–∏–ª–∫–∏)
    inventory_value[item] = stock[item] * prices.get(item, 0)

# –í–∏–≤–æ–¥–∏–º–æ —Å–ª–æ–≤–Ω–∏–∫ —ñ–∑ –∑–∞–≥–∞–ª—å–Ω–æ—é –≤–∞—Ä—Ç—ñ—Å—Ç—é –∫–æ–∂–Ω–æ–≥–æ —Ç–∏–ø—É —Ç–æ–≤–∞—Ä—É
print("\nüí∞ –ó–∞–≥–∞–ª—å–Ω–∞ –≤–∞—Ä—Ç—ñ—Å—Ç—å –∑–∞ —Ç–∏–ø–∞–º–∏ —Ç–æ–≤–∞—Ä—ñ–≤:")
print(inventory_value)

